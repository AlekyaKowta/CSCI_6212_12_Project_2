/**
 * Class representing a 2D point.
 * Used across all staircase computation algorithms.
 */
class Point {
    double x;
    double y;

    public Point(double x, double y) {
        this.x = x;
        this.y = y;
    }

    @Override
    public String toString() {
        return "(" + x + ", " + y + ")";
    }
}